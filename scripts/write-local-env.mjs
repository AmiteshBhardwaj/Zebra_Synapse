import { execSync } from "node:child_process";
import { writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, "..");
const cliPath =
  process.platform === "win32"
    ? path.join(projectRoot, "node_modules", ".bin", "supabase.cmd")
    : path.join(projectRoot, "node_modules", ".bin", "supabase");

const siteUrl = process.env.VITE_SITE_URL?.trim() || "http://localhost:5173";
const envFilePath = path.join(projectRoot, ".env.local");

function fail(message) {
  console.error(message);
  process.exit(1);
}

function parseEnvOutput(output) {
  const entries = new Map();

  for (const rawLine of output.split(/\r?\n/)) {
    const line = rawLine.trim();
    if (!line || line.startsWith("#")) continue;

    const equalsIndex = line.indexOf("=");
    if (equalsIndex === -1) continue;

    entries.set(line.slice(0, equalsIndex), line.slice(equalsIndex + 1));
  }

  return entries;
}

try {
  const command = [
    `"${cliPath}"`,
    "status",
    "-o",
    "env",
    "--override-name",
    "api.url=VITE_SUPABASE_URL",
    "--override-name",
    "auth.anon_key=VITE_SUPABASE_ANON_KEY",
  ].join(" ");

  const output = execSync(command, {
    cwd: projectRoot,
    encoding: "utf8",
    stdio: ["ignore", "pipe", "pipe"],
  });

  const env = parseEnvOutput(output);
  const supabaseUrl = env.get("VITE_SUPABASE_URL");
  const supabaseAnonKey = env.get("VITE_SUPABASE_ANON_KEY");

  if (!supabaseUrl || !supabaseAnonKey) {
    fail(
      [
        "Unable to read local Supabase credentials from `supabase status`.",
        "Run `npm run supabase:start` first and make sure the stack is healthy.",
      ].join("\n"),
    );
  }

  const fileContents = [
    "# Generated by `npm run env:local` against the running local Supabase stack.",
    `VITE_SUPABASE_URL=${supabaseUrl}`,
    `VITE_SUPABASE_ANON_KEY=${supabaseAnonKey}`,
    `VITE_SITE_URL=${siteUrl}`,
    "",
  ].join("\n");

  writeFileSync(envFilePath, fileContents, "utf8");
  console.log(`Wrote ${path.basename(envFilePath)} with local Supabase credentials.`);
} catch (error) {
  const details =
    error &&
    typeof error === "object" &&
    "stderr" in error &&
    typeof error.stderr === "string" &&
    error.stderr.trim().length
      ? error.stderr.trim()
      : error instanceof Error
        ? error.message
        : String(error);

  fail(
    [
      "Unable to generate `.env.local` from the local Supabase stack.",
      "Run `npm run supabase:start` first. If Docker is not running, start Docker Desktop and retry.",
      details,
    ].join("\n\n"),
  );
}
